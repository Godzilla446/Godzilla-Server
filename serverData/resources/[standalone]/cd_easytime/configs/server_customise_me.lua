function Notification(source, notif_type, message)
    if source and notif_type and message then
        if notif_type == 1 then
            TriggerClientEvent('QBCore:Notify', source, message, 'success', 5000)
        elseif notif_type == 2 then
            TriggerClientEvent('QBCore:Notify', source, message, 'info', 5000)
        elseif notif_type == 3 then
            TriggerClientEvent('QBCore:Notify', source, message, 'error', 5000)
        end
    end
end